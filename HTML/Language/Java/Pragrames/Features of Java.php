<h3 class="font-weight-bold">Platform Independent :-</h3>
<div class="card">
<div class="card-body">
<h5>Java is platform independent because it is different from other languages like C,C++, etc. 
  which are compiled into platform specific machines while Java is a write once, run anywhere language. 
  A platform is the hardware or software environment in which a program runs.</h5>
  <div class ="code-codebar">
<hr>
  
  <li> There are two types of platforms software-based and hardware-based. 
    Java provides a software-based platform.</li>
    <li> The Java platform differs from most other platforms in the sense that it is a software-based 
      platform that runs on top of other hardware-based platforms. It has two components:</li>
<ul>
  <hr>
<h5>1. Runtime Environment</h5>
<h5>2. MAPI(Application Programming Interface)</h5>

</ul>
<hr>
</div>
<h5>Java code can be executed on multiple platforms, for example, Windows, Linux, Sun Solaris, Mac/OS, etc. Java code is compiled by the compiler and converted into bytecode. This bytecode is a platform-independent code because it can be run on multiple platforms, i.e., Write Once and Run Anywhere (WORA).</h5>
</div>
</div>
<hr>
<h3>Secured :-</h3>

<div class="card">
<div class="card-body">
<h5> Java is best known for its security. With Java, we can develop virus-free systems. Java is secured because:
</h5>
<div class ="code-codebar">
<hr>

<li>No explicit pointer</li>
<li>Java Programs run inside a virtual machine sandbox</li>
<li>Classloader :- Classloader in Java is a part of the Java Runtime Environment (JRE) which is used to load Java classes into the Java Virtual Machine dynamically. It adds security by separating the package for the classes of the local file system from those that are imported from network sources.</li>
<li>Bytecode Verifier :- It checks the code fragments for illegal code that can violate access rights to objects.</li>
<li>Security Manager :- It determines what resources a class can access such as reading and writing to the local disk.</li>

<hr>
</div>
<h5>Java language provides these securities by default. Some security can also be provided by an application developer explicitly through SSL, JAAS, Cryptography, etc.</h5>
</div>
</div>

<hr>
<h3>Robust :- </h3>


<div class="card">
<div class="card-body">
<h5>The English mining of Robust is strong. Java is robust because:</h5>
<div class ="code-codebar">
<hr>

  <li>It uses strong memory management.</li>
  <li>There is a lack of pointers that avoids security problems.</li>
  <li>Java provides automatic garbage collection which runs on the Java Virtual Machine to get rid of objects which are not being used by a Java application anymore.</li>
  <li>There are exception handling and the type checking mechanism in Java. All these points make Java robust.</li>


<hr>
</div>
</div>
</div>

<hr>
<h3 class="font-weight-bold">Architecture-neutral :-</h3>


<div class="card">
<div class="card-body">
<h5>Java is architecture neutral because there are no implementation dependent features, for example, the size of primitive types is fixed.
</h5>
<h5>In C programming, int data type occupies 2 bytes of memory for 32-bit architecture and 4 bytes of memory for 64-bit architecture.
   However, it occupies 4 bytes of memory for both 32 and 64-bit architectures in Java.
</h5>  
</div>
</div>

<hr>
<h3 class="font-weight-bold">Portable :-</h3>

<div class="card">
<div class="card-body">
<h5>Java is portable because it facilitates you to carry the Java bytecode to any platform. It doesn't require any implementation.</h5>
</div>
</div>

<hr>
<h3 class="font-weight-bold">High-performance :-</h3>
<div class="card">
<div class="card-body">
<h5>Java is faster than other traditional interpreted programming languages because Java bytecode is "close" to native code. It is still a little bit slower than a compiled language (e.g., C++). Java is an interpreted language that is why it is slower than compiled languages, e.g., C, C++, etc.</h5>
</div>
</div>

<hr>
<h3 class="font-weight-bold">Distributed :-</h3>

<div class="card">
<div class="card-body">
<h5>Java is distributed because it facilitates users to create distributed applications in Java. RMI and EJB are used for creating distributed applications. This feature of Java makes us able to access files by calling the methods from any machine on the internet.</h5>
</div>
</div>

<hr>
<h3 class="font-weight-bold">Multi-threaded :-</h3>

<div class="card">
<div class="card-body">
<h5>A thread is like a separate program, executing concurrently. We can write Java programs that deal with many tasks at once by defining multiple threads. The main advantage of multi-threading is that it doesn't occupy memory for each thread. It shares a common memory area. Threads are important for multi-media, Web applications, etc.</h5>
</div>
</div>

<hr>
<h3 class="font-weight-bold">Dynamic :-</h3>

<div class="card">
<div class="card-body">
<h5>Java is a dynamic language. It supports the dynamic loading of classes. It means classes are loaded on demand. It also supports functions from its native languages, i.e., C and C++.</h5>
</div>
