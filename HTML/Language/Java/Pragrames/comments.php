<h3 class="text-center font-weight-bold">Types of Java Comments :- </h3>
<hr>
<div class="card">
<div class="card-body">
<h4>There are three types of comments in Java.</h4>
<ul>
<h5>1. Single Line Comment</h5>
<h5>2. Multi Line Comment</h5>
<h5>3. Documentation Comment</h5>
</ul>
</div>
</div>
<hr>
<h3>1. Single Line Comment :-</h3>
<hr>
<div class="card">
<div class="card-body">
<h5> The single-line comment is used to comment only one line of the code. 
     It is the widely used and easiest way of commenting the statements.
</h5>
<h5> Single line comments starts with two forward slashes(//). 
     Any text in front of // is not executed by Java.
</h5>    
<hr>
<h3>Syntax: </h3>
<pre class="code-codebar">
<font color ="color:blue">
 //This is single line comment
</font>
</pre>
</div>
</div>


<hr>
<h3>2. Multi Line Comment :-</h3>
<hr>
<div class="card">
<div class="card-body">
<ul>
<li> The multi-line comment is used to comment multiple lines of code.
     It can be used to explain a complex code snippet or to comment multiple lines of code at a time (as it will be difficult to use single-line comments there).
</li>
<li> Multi-line comments are placed between /* and */. Any text between /* and */ is not executed by Java.
</li>   
</ul> 
<hr>
<h3>Syntax: </h3>
<pre class="code-codebar">
<font color ="color:blue">
/*
 This
 is
 multi line
 comment
 */
</font>
</pre>
</div>
</div>


<hr>
<h3>3. Documentation Comment :-</h3>
<hr>
<div class="card">
<div class="card-body">
  <ul>
<li> Documentation comments are usually used to write large programs for a project or software application as it helps to create documentation API.
These APIs are needed for reference, i.e., which classes, methods, arguments, etc., are used in the code.
</li>
<li>To create documentation API, we need to use the "javadoc tool".
The documentation comments are placed between /** and */.
</li>   
</ul> 
<hr>
<h3>Syntax: </h3>
<pre class="code-codebar">
<font color ="color:blue">
/**  
    * 
    *We can use various tags to depict the parameter 
    *or heading or author name 
    *We can also use HTML tags   
    * 
    */
</font>
</pre>
</div>
